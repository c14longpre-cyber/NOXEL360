export function getLandingForLang(lang: string): Record<string, string> {
  const short = (lang || "en").split("-")[0].toLowerCase();
  if (short === "fr") return FR;
  if (short === "es") return ES;
  if (short === "de") return DE;
  if (short === "it") return IT;
  if (short === "pt") return PT;
  if (short === "zh") return ZH;
  if (short === "ar") return AR;
  if (short === "hi") return HI;
  if (short === "ru") return RU;
  if (short === "ja") return JA;
  if (short === "ko") return KO;
  return EN;
}

export const orderedLandingKeys: string[] = Object.keys(EN).sort((a, b) => a.localeCompare(b));
export const landingRegistry: Record<string, string> = EN;

/** @deprecated Utiliser getLandingForLang(language) depuis le composant */
export function getLandingRegistry(): Record<string, string> {
  try {
    const stored = localStorage.getItem("noxel.language");
    return getLandingForLang(stored || "en");
  } catch {
    return EN;
  }
}